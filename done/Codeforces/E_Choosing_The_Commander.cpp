int trie[MAXN * 30][2], cnt[MAXN * 30];
int id;

void node(int &x) {
    if (!x) x = ++id;
}

void insert(int x, int val) {
    int cur = 0;
    for (int i = 29; i >= 0; --i) {
        node(trie[cur][x >> i & 1]);
        cur = trie[cur][x >> i & 1];
        cnt[cur] += val;
    }
}

int query(int x, int val) {
    int cur = 0, res = 0;
    for (int i = 29; i >= 0; --i) {
        int b = x >> i & 1;
        node(trie[cur][b]);
        node(trie[cur][b ^ 1]);
        if (val >> i & 1) {
            res += cnt[trie[cur][b]];
            cur = trie[cur][b ^ 1];
        } else {
            cur = trie[cur][b];
        }
    }
    return res;
}

void solve() {
    int q; cin >> q;
    while (q--) {
        int t, p; cin >> t >> p;
        if (t == 1) insert(p, 1);
        else if (t == 2) insert(p, -1);
        else {
            int l; cin >> l;
            cout << query(p, l) << '\n';
        }
    }
}
 
